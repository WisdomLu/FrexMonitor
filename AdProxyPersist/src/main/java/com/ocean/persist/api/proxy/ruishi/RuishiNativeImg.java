package com.ocean.persist.api.proxy.ruishi;

public class RuishiNativeImg {

	private String url;//	string	是	图片地址	 
	private int w	;//int	是	宽	 
	private int h	;//int	是	高	 
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getW() {
		return w;
	}
	public void setW(int w) {
		this.w = w;
	}
	public int getH() {
		return h;
	}
	public void setH(int h) {
		this.h = h;
	}

}
