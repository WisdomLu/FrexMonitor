package com.ocean.persist.api.proxy.weiyu;

public class WeiyuImg {
	private String url	;//string	是	图片地址
	private int w	;//int	是	宽
	private int h	;//int	是	高
	public String getUrl() {
		return url;
	}
	public int getW() {
		return w;
	}
	public int getH() {
		return h;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public void setW(int w) {
		this.w = w;
	}
	public void setH(int h) {
		this.h = h;
	}
}
