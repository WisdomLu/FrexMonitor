
package com.ocean.persist.app.dis.zk;
public class ZKAppSpaceInfo {


	  private int adSpaceId; // required
	  private int appId; // optional
	  private int spaceWidth; // required
	  private int spaceHeight; // required
		public int getAdSpaceId() {
			return adSpaceId;
		}
		public void setAdSpaceId(int adSpaceId) {
			this.adSpaceId = adSpaceId;
		}
		public int getAppId() {
			return appId;
		}
		public void setAppId(int appId) {
			this.appId = appId;
		}
		public int getSpaceWidth() {
			return spaceWidth;
		}
		public void setSpaceWidth(int spaceWidth) {
			this.spaceWidth = spaceWidth;
		}
		public int getSpaceHeight() {
			return spaceHeight;
		}
		public void setSpaceHeight(int spaceHeight) {
			this.spaceHeight = spaceHeight;
		}
		

}

